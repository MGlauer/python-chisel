def get_all_files(path):
    for root, dirs, files in os.walk(path):
        for file in files:
            if not file == 'README':
                yield os.path.join(root, file)

def all_problems(Session):
    files = get_all_files('data/TPTP/Problems')
    for f in files:
        process_problem(f)



def process_problem(path):
    engine = get_engine()
    Session = sessionmaker(bind=engine)
    build.load_problem(Session, path)


def process_axiom_set(path):
    engine = get_engine()
    Session = sessionmaker(bind=engine)
    build.load_axiomset(Session, path)

